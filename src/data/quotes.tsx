// Utility functions for quote management
export const getRandomQuotes = (count: number = 3): Quote[] => {
  const shuffled = [...quotesData].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

export const getQuotesByTopic = (topic: string, count: number = 3): Quote[] => {
  const searchTerm = topic.toLowerCase();
  
  const filteredQuotes = quotesData.filter(quote => 
    quote.category.toLowerCase().includes(searchTerm) || 
    quote.text.toLowerCase().includes(searchTerm) ||
    quote.author.toLowerCase().includes(searchTerm)
  );
  
  // If no matches found, return random quotes
  if (filteredQuotes.length === 0) {
    return getRandomQuotes(count);
  }
  
  // Shuffle and return requested count
  const shuffled = filteredQuotes.sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

export const getAllCategories = (): string[] => {
  const categories = quotesData.map(quote => quote.category);
  return [...new Set(categories)].sort();
};
